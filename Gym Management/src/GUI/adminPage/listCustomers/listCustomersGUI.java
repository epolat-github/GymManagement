    private void prepareTable() {
        //set colon names
        model = new DefaultTableModel(new String[]{"Customer ID", "Customer Name",
            "Customer Surname", "Gender", "Birth Year", "Course Code",
            "Section Number", "Section Count"}, 0);
        try {
            ResultSet rs = conn.listCustomers();
            while (rs.next()) {
                long cusID = rs.getLong("customer覺d");
                String cusName = rs.getString("name");
                String cusSurname = rs.getString("surname");
                String cusGender = rs.getString("gender");
                int cusBirth = rs.getInt("b覺rthyear");
                String courseCode = rs.getString("code");
                int secNumber = rs.getInt("sect覺onnumber");
                int secTime = rs.getInt("t覺me");
                model.addRow(new Object[]{cusID, cusName, cusSurname, cusGender,
                    cusBirth, courseCode, secNumber, secTime});

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Can't read the informations from database!");
        }
    }

